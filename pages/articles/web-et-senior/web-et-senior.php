    <h1 class="secbloc_h1">Qu’en est-il de ceux pour qui consulter des emails est un fardeau ?</h1>
    <p class="secbloc_p">Les personnes n’ayant jamais connus Internet sont certains moins nombreuse mais une partie de la population est plus touchée par ce problème, Les personnes dites « âgées ou séniors » représente la part la plus importante de la population française ayant des difficultés avec le numérique et subissent les effets de l’illectronisme. En cette période de crise sanitaires ces problèmes d’accessibilité ont été mis en exergues, l’illectronisme accentue l’isolement des personnes « âgées », selon l’INSEE l’accès au réseau sociaux et aux moyens de communication via Internet permet de garder contact avec sa famille et d’éviter l’isolement.</p>


            Les métiers évoluent rapidement, l’utilisation de l’informatique prend de plus en plus de place au quotidien et cela entraine une exclusion involontaire des personnes qui ne maitrisent pas ces outils.</span><br><br>
            De plus, viennent s’ajouter aux problèmes de compréhension numérique, les problèmes d’accessibilité physique. <br><br>
            <span class="orange">
            Les personnes atteintes de certains handicaps ne possèdent pas les capacités pour naviguer sur Internet.</span> <br><br>
            Pour une personne mal-voyante, son handicap va lui imposer des conditions particulières de navigation. 


            <h1>Qui est touché par ce sujet ?</h1>
    </div>

    <div class="englob">
        <div></div>
            <div><p><span class="orange">En France, en 2020 19% des français disent être victimes d’illectronisme et de subir les effets des fractures numériques.</span><br><br>
            Selon une enquête commandée par le sénat, la France est placée 15ème sur 27 des pays de l’UE en termes d’accessibilité numérique.  L’illectronisme touchent tout le monde.
            </p> 

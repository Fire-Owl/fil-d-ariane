    <h1><span class="orange">L'illectronisme</span><br>Qu'est-ce que l'illectronisme ?</h1>
</div>

    <div class="englob">
        <div></div>
            <div><p>« Etat d’une personne qui ne maîtrise pas les compétences nécessaires à l’utilisation et à la création des ressources numérique »<br><br>
            Ce néologisme est né de la combinaison des notions d’illettrisme et d’électronique,  renvoie aux fractures numériques qui touchent prêt d’un quart de la population française.<br><br>
            Dans, l’illectronisme, on distingue les personnes qui ne savent pas se servir des outils numérique tels que les ordinateurs ou les smartphones et les personnes qui quant à eux ont des difficulté à maitriser le contenu sur Internet (remplir des formulaire en lignes, acheter sur un site web..)</p> 
            </div>
        <div><img src="../assets/images/illectronisme1img.svg" alt=""></div>
        <div></div>
    </div>
    

    <div class="englobBIS">



        <div></div>
        <div><img src="../assets/images/illectronisme2img.svg" alt=""></div>
        <div><p>Dans une société de plus en plus numérique, dans notre Start-up Nation, <span class="orange">l’acquisition de compétences informatique est devenue nécessaire et indispensable pour évoluer en tant que citoyen.</span><br><br>
        Mais pour certains de nos concitoyens ces compétences si indispensable soit elles, ne sont pas connus pour diverses raisons,<span class="orange">créant donc de nouvelles fractures et inégalités.</span>
        </p></div>
        <div></div>
        </div>


        <div class="englob">
        <div></div>
            <div><p>Un Plan d’Action mis en place par le gouvernement vise notamment à dématérialiser les 250 démarches publiques les plus utilisées, ce souhait met en avant les fractures numériques présentent en France. Mais cela ne s’arrête pas uniquement aux démarches administratives, l’utilisation d’Internet couvrent toutes les actions qui par avant demandais un contact physique, maintenant pour chercher du travail, garder contact avec sa famille le numérique est quasi-obligatoire.<br><br>
            Cet sur utilisation du numérique laisse de côté de nombreuses personnes.<span class="orange">L’illectronisme ne touche pas que les personnes âgées ou les zones blanches mais elle bien tout le monde peu en être victime.</span>
            </p> 
            </div>
            <div><img src="../assets/images/illectronisme3img.svg" alt=""></div>

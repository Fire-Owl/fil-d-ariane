        <div><p><span class="orange">Les premiers à subir les exclusions numériques sont les personnes qui n’ont pas grandi ou n’ont pas participé à l’émergence du web.</span><br><br>
         Pour ceux qui découvrent le numérique et Internet, l’afflux d’informations et de connaissances nécessaires à leurs utilisations semblent insurmontable à apprendre, ce qui entrainent et crée un sentiment d’abandon numérique.<br><br>
        L’illectronisme touche aussi ceux qui doivent se servir d’internet dans leurs quotidiens.<br><br><span class="orange">
        Ces fractures ont été un peu plus pointé du doigt en ces périodes de confinements, beaucoup de travailleurs ont dû télé-travailler.</span>




